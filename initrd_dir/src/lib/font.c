static font_t g_font;

kernel_status_t font_init(void)
{
	g_font.width = FONT_WIDTH;
	g_font.height = FONT_HEIGHT;

	/* If CHARSET is present, copy it; otherwise return error.
	 * (Assumes the full CHARSET array is present in the source.)
	 */
	memcpy(g_font.data, CHARSET, sizeof(g_font.data));
	return KERNEL_OK;
}

const font_t *font_get_default(void)
{
	return &g_font;
}

kernel_status_t font_render_char(char c, u16 x, u16 y, vbe_color_t fg_color,
				 vbe_color_t bg_color, const font_t *font)
{
	if (!font || !vbe_is_available())
		return KERNEL_ERROR;

	vbe_device_t *dev = vbe_get_device();
	if (!dev)
		return KERNEL_ERROR;

	if (x + font->width > dev->width || y + font->height > dev->height)
		return KERNEL_INVALID_PARAM;

	u8 idx = (u8)c;

	for (u8 row = 0; row < font->height; row++) {
		u8 bits = font->data[idx][row];
		for (u8 col = 0; col < font->width; col++) {
			u8 mask = 1 << (7 - col);
			vbe_color_t colv = (bits & mask) ? fg_color : bg_color;
			if (vbe_put_pixel(x + col, y + row, colv) != KERNEL_OK)
				return KERNEL_ERROR;
		}
	}
	return KERNEL_OK;
}

kernel_status_t font_render_string(const char *str, u16 x, u16 y,
				   vbe_color_t fg_color, vbe_color_t bg_color,
				   const font_t *font)
{
	if (!str || !font)
		return KERNEL_INVALID_PARAM;

	u16 cur_x = x;
	u16 cur_y = y;

	for (const char *p = str; *p; ++p) {
		if (*p == '\n') {
			cur_x = x;
			cur_y += font->height;
			continue;
		}
		if (*p == '\r') {
			cur_x = x;
			continue;
		}

		if (font_render_char(*p, cur_x, cur_y, fg_color, bg_color, font) != KERNEL_OK)
			return KERNEL_ERROR;

		cur_x += font->width;
		vbe_device_t *dev = vbe_get_device();
		if (dev && cur_x + font->width > dev->width) {
			cur_x = x;
			cur_y += font->height;
		}
	}
	return KERNEL_OK;
}

void text_context_init(text_context_t *ctx, u16 x, u16 y, vbe_color_t fg_color,
		       vbe_color_t bg_color)
{
	if (!ctx) return;

	ctx->x = x;
	ctx->y = y;
	ctx->fg_color = fg_color;
	ctx->bg_color = bg_color;
	ctx->font = font_get_default();
}

kernel_status_t text_context_putchar(text_context_t *ctx, char c)
{
	if (!ctx || !ctx->font)
		return KERNEL_INVALID_PARAM;

	if (c == '\n') {
		text_context_newline(ctx);
		return KERNEL_OK;
	}
	if (c == '\r') {
		ctx->x = 0;
		return KERNEL_OK;
	}

	if (font_render_char(c, ctx->x, ctx->y, ctx->fg_color, ctx->bg_color, ctx->font) != KERNEL_OK)
		return KERNEL_ERROR;

	ctx->x += ctx->font->width;
	vbe_device_t *dev = vbe_get_device();
	if (dev && ctx->x + ctx->font->width > dev->width)
		text_context_newline(ctx);

	return KERNEL_OK;
}

kernel_status_t text_context_print(text_context_t *ctx, const char *str)
{
	if (!ctx || !str)
		return KERNEL_INVALID_PARAM;

	for (const char *p = str; *p; ++p) {
		if (text_context_putchar(ctx, *p) != KERNEL_OK)
			return KERNEL_ERROR;
	}
	return KERNEL_OK;
}

void text_context_newline(text_context_t *ctx)
{
	if (!ctx || !ctx->font) return;
	ctx->x = 0;
	ctx->y += ctx->font->height;
}

void text_context_set_position(text_context_t *ctx, u16 x, u16 y)
{
	if (!ctx) return;
	ctx->x = x;
	ctx->y = y;
}

void text_context_set_colors(text_context_t *ctx, vbe_color_t fg_color, vbe_color_t bg_color)
{
	if (!ctx) return;
	ctx->fg_color = fg_color;
	ctx->bg_color = bg_color;
}
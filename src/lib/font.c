static font_t g_font;

kernel_status_t font_init(void) {
    g_font.width = FONT_WIDTH;
    g_font.height = FONT_HEIGHT;
    
    for (int i = 0; i < FONT_CHARS; i++) {
        for (int j = 0; j < FONT_HEIGHT; j++) {
            if (i < 0x80) {
                g_font.data[i][j] = default_font_data[i][j];
            } else {
                g_font.data[i][j] = (j % 2 == 0) ? 0xAA : 0x55;
            }
        }
    }
    
    return KERNEL_OK;
}

const font_t* font_get_default(void) {
    return &g_font;
}

kernel_status_t font_render_char(char c, u16 x, u16 y, vbe_color_t fg_color, vbe_color_t bg_color, const font_t* font) {
    if (!font || !vbe_is_available()) {
        return KERNEL_ERROR;
    }
    
    vbe_device_t* vbe_dev = vbe_get_device();
    if (!vbe_dev) {
        return KERNEL_ERROR;
    }
    
    if (x + font->width > vbe_dev->width || y + font->height > vbe_dev->height) {
        return KERNEL_INVALID_PARAM;
    }
    
    u8 char_index = (u8)c;
    
    for (u8 row = 0; row < font->height; row++) {
        u8 font_row = font->data[char_index][row];
        
        for (u8 col = 0; col < font->width; col++) {
            u8 bit = 7 - col;
            vbe_color_t pixel_color = (font_row & (1 << bit)) ? fg_color : bg_color;
            
            kernel_status_t status = vbe_put_pixel(x + col, y + row, pixel_color);
            if (status != KERNEL_OK) {
                return status;
            }
        }
    }
    
    return KERNEL_OK;
}

kernel_status_t font_render_string(const char* str, u16 x, u16 y, vbe_color_t fg_color, vbe_color_t bg_color, const font_t* font) {
    if (!str || !font) {
        return KERNEL_INVALID_PARAM;
    }
    
    u16 current_x = x;
    u16 current_y = y;
    
    for (const char* p = str; *p != '\0'; p++) {
        if (*p == '\n') {
            current_x = x;
            current_y += font->height;
            continue;
        }
        
        if (*p == '\r') {
            current_x = x;
            continue;
        }
        
        kernel_status_t status = font_render_char(*p, current_x, current_y, fg_color, bg_color, font);
        if (status != KERNEL_OK) {
            return status;
        }
        
        current_x += font->width;
        
        vbe_device_t* vbe_dev = vbe_get_device();
        if (vbe_dev && current_x + font->width > vbe_dev->width) {
            current_x = x;
            current_y += font->height;
        }
    }
    
    return KERNEL_OK;
}

void text_context_init(text_context_t* ctx, u16 x, u16 y, vbe_color_t fg_color, vbe_color_t bg_color) {
    if (!ctx) return;
    
    ctx->x = x;
    ctx->y = y;
    ctx->fg_color = fg_color;
    ctx->bg_color = bg_color;
    ctx->font = font_get_default();
}

kernel_status_t text_context_putchar(text_context_t* ctx, char c) {
    if (!ctx || !ctx->font) {
        return KERNEL_INVALID_PARAM;
    }
    
    if (c == '\n') {
        text_context_newline(ctx);
        return KERNEL_OK;
    }
    
    if (c == '\r') {
        ctx->x = 0;
        return KERNEL_OK;
    }
    
    kernel_status_t status = font_render_char(c, ctx->x, ctx->y, ctx->fg_color, ctx->bg_color, ctx->font);
    if (status != KERNEL_OK) {
        return status;
    }
    
    ctx->x += ctx->font->width;
    
    vbe_device_t* vbe_dev = vbe_get_device();
    if (vbe_dev && ctx->x + ctx->font->width > vbe_dev->width) {
        text_context_newline(ctx);
    }
    
    return KERNEL_OK;
}

kernel_status_t text_context_print(text_context_t* ctx, const char* str) {
    if (!ctx || !str) {
        return KERNEL_INVALID_PARAM;
    }
    
    for (const char* p = str; *p != '\0'; p++) {
        kernel_status_t status = text_context_putchar(ctx, *p);
        if (status != KERNEL_OK) {
            return status;
        }
    }
    
    return KERNEL_OK;
}

void text_context_newline(text_context_t* ctx) {
    if (!ctx || !ctx->font) return;
    
    ctx->x = 0;
    ctx->y += ctx->font->height;
}

void text_context_set_position(text_context_t* ctx, u16 x, u16 y) {
    if (!ctx) return;
    
    ctx->x = x;
    ctx->y = y;
}

void text_context_set_colors(text_context_t* ctx, vbe_color_t fg_color, vbe_color_t bg_color) {
    if (!ctx) return;
    
    ctx->fg_color = fg_color;
    ctx->bg_color = bg_color;
}
